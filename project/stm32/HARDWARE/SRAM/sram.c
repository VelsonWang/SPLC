//输出
void ExtIO_Out1(uint16_t data)
{
	CSn = 0;	
	O1_CS = 1;
	
	if(data&0x0001) D0 = 1;
	else D0 = 0;
	if(data&0x0002) D1 = 1;
	else D1 = 0;
	if(data&0x0004) D2 = 1;
	else D2 = 0;
	if(data&0x0008) D3 = 1;
	else D3 = 0;
	if(data&0x0010) D4 = 1;
	else D4 = 0;
	if(data&0x0020) D5 = 1;
	else D5 = 0;
	if(data&0x0040) D6 = 1;
	else D6 = 0;
	if(data&0x0080) D7 = 1;
	else D7 = 0;
	if(data&0x0100) D8 = 1;
	else D8 = 0;
	if(data&0x0200) D9 = 1;
	else D9 = 0;
	if(data&0x0400) D10 = 1;
	else D10 = 0;
	if(data&0x0800) D11 = 1;
	else D11 = 0;
	if(data&0x1000) D12 = 1;
	else D12 = 0;
	if(data&0x2000) D13 = 1;
	else D13 = 0;
	if(data&0x4000) D14 = 1;
	else D14 = 0;
	if(data&0x8000) D15 = 1;
	else D15 = 0;
	
	delay_us(1);
	
	WRn = 0;
	
	delay_us(5);
	WRn = 1;
	O1_CS = 0;
	CSn = 1;
	
}


